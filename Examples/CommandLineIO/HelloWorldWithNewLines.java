public class HelloWorldWithNewLines {
    public static void main(String[] args) {
        System.out.print("Hello World!"); // Prints out the string, ending with a new line
        System.out.print("Is this a new line?");
        System.out.println("Maybe this one is?"); // Prints out the string, ending with a new line
        System.out.println("Or this one?");
        System.out.print("And what does this \n do?");
    }
}
